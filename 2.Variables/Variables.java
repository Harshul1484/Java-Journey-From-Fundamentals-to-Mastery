public class Variables {
    public static void main(String[] args) {
        // Integral types
        byte TypeByte = 127;          // 8-bit
        short TypeShort = 32767;      // 16-bit
        int TypeInt = 2147483647;     // 32-bit
        long TypeLong = 9223372036854775807L; // 64-bit

        // Floating-point types
        float TypeFloat = 3.14f;      // 32-bit
        double TypeDouble = 3.14159;  // 64-bit

        // Character type
        char TypeChar = 'A';          // 16-bit Unicode character

        // Boolean type
        boolean TypeBoolean = true;   // 0 or 1

        // Output
        System.out.println("byte: " + TypeByte);
        System.out.println("short: " + TypeShort);
        System.out.println("int: " + TypeInt);
        System.out.println("long: " + TypeLong);
        System.out.println("float: " + TypeFloat);
        System.out.println("double: " + TypeDouble);
        System.out.println("char: " + TypeChar);
        System.out.println("boolean: " + TypeBoolean);
    }
}

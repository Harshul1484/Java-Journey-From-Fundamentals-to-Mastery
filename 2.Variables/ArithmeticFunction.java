public class ArithmeticFunction {
    public static void main(String[] args){
      int a = 15;
      int b = 5;

      //addition
      int sum = a + b;
      System.out.println("Addition: " +sum);

      //substraction
      int diff = a - b;
      System.out.println("Substraction: " + diff);

      //multiplication
      int mul = a * b;
      System.out.println("Multiplication: " + mul);
    }
}
